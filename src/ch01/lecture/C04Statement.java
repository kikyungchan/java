package ch01.lecture;

public class C04Statement {
    // main :: main 메소드 만들기
    public static void main(String[] args) {
        // 명령문
        System.out.println("출력문1");
        // 순서대로 실행됨
        System.out.println("출력문2");
        // ;으로 끝나야함
        System.out.println("출력문3");
        System.out.println("출력문4");
        System.out.println("출력문5");
        //ctrl shift enter : 나머지 명령문 자동완성
        System.out.println("출력문6");

        // ctrl alt l : 들여쓰기 줄맞추기 (code formatting)

        //ctrl d 현재줄 복사
        System.out.println("출력문7");
        System.out.println("출력문8");
        System.out.println("출력문9");
        System.out.println("출력문10");
        System.out.println("출력문11");

        // alt shift 위아래
        System.out.println("출력문0");

        //ctrl alt enter : 윗 줄에 코드 입력
    }
}
