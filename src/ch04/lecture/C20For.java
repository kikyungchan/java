package ch04.lecture;

public class C20For {
    public static void main(String[] args) {
        //debugging

        System.out.println("코드1");
        System.out.println("코드2");
        // 빨간점은 break point 브레이크포인트 멈춤지점
        // f8로 한스텝씩 실행시켜볼수있음 for문
        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }


    }
}
